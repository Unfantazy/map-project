import {useEffect} from "react";

const Counter = ({ count, setCount })  => {

    //На любой элемент можно повесить все обработчики из нативного js (onClick, onChange)
    //Вызов функции из useState вызывает перерисовку компонента, поэтому, нажимая на кнопку, меняется число
    return (
        <div className={'counter'}>
            <button onClick={() => {
                setCount(count + 1)
            }}>Click me</button>
            <h2>{count}</h2>
        </div>
    );
}

//Есть еще хук useEffect - хук-синхронизатор, чаще всего используется для запросов на сервер

