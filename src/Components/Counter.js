//Принимает в себя 2 параметра: колбэк и зависимости. Если не указать зависимости, useEffect будет вызываться бесконечно
//Если зависимости пустые - будет срабатывать 1 раз - при загрузке Компонента
// Если указать зависимость (например, count), то useEffect будет вызываться каждый раз, когда меняется count (когда мы жмем на кнопку)

export default Counter;
